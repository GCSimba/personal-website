const storageKey = "portfolio-language";
const languageButtons = document.querySelectorAll("[data-lang]");
const textNodes = document.querySelectorAll("[data-i18n]");
const htmlNodes = document.querySelectorAll("[data-i18n-html]");
const listNodes = document.querySelectorAll("[data-list]");
const descriptionNode = document.querySelector('meta[name="description"]');
const ogTitleNode = document.querySelector('meta[property="og:title"]');
const ogDescriptionNode = document.querySelector('meta[property="og:description"]');
const lastUpdatedNode = document.querySelector(".last-updated");

function renderList(node, items) {
  node.innerHTML = items
    .map((item) => {
      const externalAttributes =
        item.href && !item.href.startsWith("#") ? ' rel="noreferrer"' : "";
      const title = item.href
        ? `<a href="${item.href}"${externalAttributes}>${item.title}</a>`
        : item.title;
      const titleTag = item.emphasis === false ? "span" : "strong";
      const meta = item.meta
        ? `<span class="meta">(${item.meta})</span>`
        : "";
      const body = item.body ? `<p>${item.body}</p>` : "";

      return `
        <li>
          <${titleTag}>${title}</${titleTag}>
          ${meta}
          ${body}
        </li>
      `;
    })
    .join("");
}

function setLanguage(lang) {
  const dictionary = content[lang] || content.en;

  document.documentElement.lang = dictionary.htmlLang;
  document.title = dictionary.pageTitle;
  descriptionNode?.setAttribute("content", dictionary.description);
  ogTitleNode?.setAttribute("content", dictionary.pageTitle);
  ogDescriptionNode?.setAttribute("content", dictionary.description);

  textNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (dictionary[key]) {
      node.textContent = dictionary[key];
    }
  });

  htmlNodes.forEach((node) => {
    const key = node.dataset.i18nHtml;
    if (dictionary[key]) {
      node.innerHTML = dictionary[key];
    }
  });

  listNodes.forEach((node) => {
    const key = node.dataset.list;
    renderList(node, dictionary[key] || []);
  });

  languageButtons.forEach((button) => {
    const isActive = button.dataset.lang === lang;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  localStorage.setItem(storageKey, lang);
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => {
    setLanguage(button.dataset.lang);
  });
});

if (lastUpdatedNode) {
  const now = new Date();
  const month = String(now.getMonth() + 1).padStart(2, "0");
  const updated = `${now.getFullYear()}-${month}`;
  lastUpdatedNode.dateTime = updated;
  lastUpdatedNode.textContent = updated;
}

setLanguage(localStorage.getItem(storageKey) || "en");
